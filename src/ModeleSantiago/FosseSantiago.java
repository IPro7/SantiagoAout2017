package ModeleSantiago;

public class FosseSantiago {
	
	private int idFosse;
	private int coordXFosse;
	private int coordYFosse;
	
	
	public FosseSantiago(int idFosse, int coordXFosse, int coordYFosse) {
		super();
		this.idFosse = idFosse;
		this.coordXFosse = coordXFosse;
		this.coordYFosse = coordYFosse;
	}


	public int getIdFosse() {
		return idFosse;
	}


	public void setIdFosse(int idFosse) {
		this.idFosse = idFosse;
	}


	public int getCoordXFosse() {
		return coordXFosse;
	}


	public void setCoordXFosse(int coordXFosse) {
		this.coordXFosse = coordXFosse;
	}


	public int getCoordYFosse() {
		return coordYFosse;
	}


	public void setCoordYFosse(int coordYFosse) {
		this.coordYFosse = coordYFosse;
	}
	
	
	
}
